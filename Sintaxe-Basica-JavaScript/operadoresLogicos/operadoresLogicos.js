// && - só da true se os dois valores forem true
var a = false && true;
console.log(a)
var b = true && false;
console.log(b)
var c = false && false;
console.log(c)

// || - só vai da falso os dois valores falsos
var a = true || false
console.log(a)
var b = false || true
console.log(b)
var c = true || true
console.log(c)

// ! - vai inverter o sentido booleano
var a = !true
console.log(a) // false
var b = !!true
console.log(b) // true

